//default value of primitives data types using static variables
public class DefaultValuesStatic
{
	static short s;
	static byte b;
	static int i;
	static long l;
	static float f;
	static double d;
	static char c;
	static boolean t;
	public static void main(String args[])
	{
		System.out.println("Demonstration of primitive data types default values using static keyword:");
		System.out.println("short:"+s);
		System.out.println("byte:"+b);
		System.out.println("int:"+i);
		System.out.println("long:"+l);
		System.out.println("float:"+f);
		System.out.println("double:"+d);
		System.out.println("char:"+c);
		System.out.println("boolean:"+t);
      }
}

